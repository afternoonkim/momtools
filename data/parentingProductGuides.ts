export function isParentingProductGuidePublic(
  guide: ParentingProductGuide,
  now = new Date(),
) {
  const publishableStatus = guide.status === "PUBLISHED" || guide.status === "SCHEDULED";
  return publishableStatus && new Date(guide.publishAt).getTime() <= now.getTime();
}

export function getPublishedParentingProductGuides(now = new Date()) {
  return parentingProductGuides
    .filter((guide) => isParentingProductGuidePublic(guide, now))
    .sort((a, b) => new Date(b.publishAt).getTime() - new Date(a.publishAt).getTime());
}

export function getParentingProductGuideBySlug(slug: string) {
  const decoded = decodeURIComponent(slug);
  return parentingProductGuides.find((guide) => guide.slug === slug || guide.slug === decoded);
}

export function getPublicParentingProductGuideBySlug(slug: string, now = new Date()) {
  const guide = getParentingProductGuideBySlug(slug);
  return guide && isParentingProductGuidePublic(guide, now) ? guide : undefined;
}

export function getParentingProductGuideByPath(pathname: string) {
  const slug = pathname.match(/^\/parenting-products\/([^/]+)$/)?.[1];
  return slug ? getParentingProductGuideBySlug(slug) : undefined;
}

export function getPublicParentingProductGuideByPath(pathname: string, now = new Date()) {
  const slug = pathname.match(/^\/parenting-products\/([^/]+)$/)?.[1];
  return slug ? getPublicParentingProductGuideBySlug(slug, now) : undefined;
}

export function getRelatedParentingProductGuides(
  current: ParentingProductGuide,
  limit = 3,
  now = new Date(),
) {
  return getPublishedParentingProductGuides(now)
    .filter((guide) => guide.slug !== current.slug)
    .sort((a, b) => Number(b.category === current.category) - Number(a.category === current.category))
    .slice(0, limit);
}
