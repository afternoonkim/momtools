export const familyFinanceArticleSlugs = familyFinanceArticles.map((a) => a.slug);

export function getFamilyFinanceArticle(slug: string) {
  return familyFinanceArticles.find((a) => a.slug === slug);
}

export function getRelatedFamilyFinanceArticles(slug: string, limit = 4) {
  const current = getFamilyFinanceArticle(slug);
  if (!current) return familyFinanceArticles.slice(0, limit);
  const related = current.related
    .map((s) => getFamilyFinanceArticle(s))
    .filter((a): a is FamilyFinanceArticle => Boolean(a));
  if (related.length >= limit) return related.slice(0, limit);
  const others = familyFinanceArticles.filter(
    (a) => a.slug !== slug && !current.related.includes(a.slug),
  );
  return [...related, ...others].slice(0, limit);
}
