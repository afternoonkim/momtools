function compactProductName(name: string) {
  return name
    .replace(/\([^)]*\)/g, "")
    .replace(/\d+mg/gi, "")
    .replace(/\d+ml/gi, "")
    .replace(/\d+g/gi, "")
    .replace(/\d+정/g, "")
    .replace(/\d+캡슐/g, "")
    .replace(/\d+포/g, "")
    .replace(/\d+매입/g, "")
    .replace(/\d+매/g, "")
    .replace(/\*.*$/g, "")
    .trim();
}

