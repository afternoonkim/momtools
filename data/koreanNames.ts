export function getKoreanNameBySlug(slug: string) {
  const normalizedSlug = decodeURIComponent(slug).trim().toLowerCase();
  return pureKoreanNames.find(
    (item) => item.slug === normalizedSlug || item.name === normalizedSlug || encodeURIComponent(item.name) === slug
  );
}