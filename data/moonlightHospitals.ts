function stripAdministrativeSuffix(value: string) {
  return value
    .replace(/특별자치도$|특별자치시$|광역시$|특별시$/g, "")
    .replace(/도$|시$|군$|구$/g, "")
    .trim();
}

function makeMoonlightSlug(value: string) {
  return value.trim().replace(/\s+/g, "-");
}

function isUsefulCityName(city: string) {
  // 세종처럼 엑셀 주소에서 도로명이 시군구 자리에 들어온 데이터는 지역 SEO 페이지로 만들지 않습니다.
  return city.length > 0 && !/[0-9]/.test(city) && !city.endsWith("로") && !city.endsWith("길");
}

