export function getMoonlightHospitalAreaPath(areaOrSlug: MoonlightHospitalArea | string) {
  const slug = typeof areaOrSlug === "string" ? areaOrSlug : areaOrSlug.slug;
  return `/moonlight-hospitals/${encodeURIComponent(slug)}`;
}

export function getMoonlightHospitalAreaBySlug(slug: string) {
  const decodedSlug = decodeURIComponent(slug).trim();
  return moonlightHospitalAreas.find((area) => area.slug === decodedSlug);
}

export function getMoonlightHospitalSiblingAreas(area: MoonlightHospitalArea, limit = 12) {
  if (area.kind === "region") {
    return moonlightHospitalCityAreas.filter((candidate) => candidate.regionShort === area.regionShort).slice(0, limit);
  }
  const sameRegion = moonlightHospitalCityAreas.filter((candidate) => candidate.regionShort === area.regionShort && candidate.slug !== area.slug);
  const regionPage = moonlightHospitalRegionAreas.find((candidate) => candidate.regionShort === area.regionShort);
  return [...(regionPage ? [regionPage] : []), ...sameRegion].slice(0, limit);
}
