export function getRecipesByStage(stage: BabyFoodStage) {
  return babyFoodRecipes.filter((item) => item.stage === stage);
}

export function getRecipe(slug: string) {
  return babyFoodRecipes.find((item) => item.slug === slug);
}

export function getRecipeCategories(stage: BabyFoodStage) {
  return Array.from(new Set(getRecipesByStage(stage).map((item) => item.category)));
}

export function buildRecipeSummary(recipe: BabyFoodRecipe) {
  return `${recipe.title} · ${stageLabels[recipe.stage]} · ${recipe.category}. ${recipe.summary}`;
}
