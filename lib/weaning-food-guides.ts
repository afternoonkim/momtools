function normalizeFoodText(value: string) {
  return value.replace(/\s+/g, "").trim().toLowerCase();
}

export function findWeaningFoodGuides(input: string, limit = 5) {
  const normalizedInput = normalizeFoodText(input);
  if (!normalizedInput) return [];

  const matches: WeaningFoodGuide[] = [];
  for (const guide of weaningFoodGuides) {
    const names = [guide.name, ...guide.aliases]
      .map(normalizeFoodText)
      .filter(Boolean);
    if (
      names.some(
        (name) =>
          normalizedInput.includes(name) || name.includes(normalizedInput),
      )
    ) {
      matches.push(guide);
      if (matches.length >= limit) break;
    }
  }

  return matches;
}
